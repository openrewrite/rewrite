package org.openrewrite.benchmarks.recipe;

import org.openjdk.jmh.annotations.*;
import org.openjdk.jmh.infra.Blackhole;
import org.openjdk.jmh.profile.GCProfiler;
import org.openjdk.jmh.runner.Runner;
import org.openjdk.jmh.runner.RunnerException;
import org.openjdk.jmh.runner.options.Options;
import org.openjdk.jmh.runner.options.OptionsBuilder;
import org.openrewrite.config.ClasspathScanningLoader;
import org.openrewrite.config.Environment;
import org.openrewrite.config.RecipeDescriptor;
import org.openrewrite.marketplace.RecipeClassLoader;

import java.io.IOException;
import java.nio.file.Files;
import java.nio.file.Path;
import java.nio.file.Paths;
import java.util.*;
import java.util.concurrent.TimeUnit;
import java.util.stream.Collectors;

/**
 * Benchmarks for recipe discovery via classpath scanning.
 * <p>
 * Measures the cost of {@code Environment.builder().scanJar()} and
 * full-classloader scanning used during recipe marketplace generation.
 * <p>
 * Run with: {@code ./gradlew :rewrite-benchmarks:jmh -Pjmh.includes="RecipeLoadingBenchmark"}
 */
@Fork(1)
@Measurement(iterations = 3)
@Warmup(iterations = 1)
@BenchmarkMode(Mode.AverageTime)
@OutputTimeUnit(TimeUnit.MILLISECONDS)
@State(Scope.Benchmark)
public class RecipeLoadingBenchmark {

    Path recipeJar;
    List<Path> dependencyPaths;

    Path rewriteSpringJar;
    List<Path> rewriteSpringDependencyPaths;

    @Setup(Level.Trial)
    public void setup() throws IOException {
        // Parse java.class.path to find the rewrite-java shadow JAR and dependencies.
        String classpath = System.getProperty("java.class.path");
        List<Path> allPaths = Arrays.stream(classpath.split(System.getProperty("path.separator")))
                .map(Paths::get)
                .filter(Files::exists)
                .collect(Collectors.toList());

        for (Path p : allPaths) {
            String name = p.getFileName().toString();
            // The shadow JAR (rewrite-java-8.80.0-SNAPSHOT.jar), not version-specific
            // JARs like rewrite-java-21-*.jar or auxiliary JARs like rewrite-java-lombok-*.jar
            if (name.startsWith("rewrite-java-") && name.endsWith(".jar") &&
                    !name.matches("rewrite-java-\\d{1,2}-.*") &&
                    !name.contains("test") && !name.contains("tck") && !name.contains("lombok")) {
                recipeJar = p;
                break;
            }
        }
        if (recipeJar == null) {
            throw new IllegalStateException("Could not find rewrite-java JAR on classpath. " +
                    "Available: " + allPaths.stream().map(p -> p.getFileName().toString())
                    .filter(n -> n.contains("rewrite")).collect(Collectors.joining(", ")));
        }

        dependencyPaths = allPaths.stream()
                .filter(p -> !p.equals(recipeJar))
                .collect(Collectors.toList());

        // Load the rewrite-spring classpath from the file generated by the Gradle build.
        // The first entry matching "rewrite-spring" is the recipe JAR; the rest are dependencies.
        Path classpathFile = Paths.get(System.getProperty("user.dir"), "rewrite-benchmarks", "build", "recipeModuleClasspath.txt");
        if (!Files.exists(classpathFile)) {
            // Try relative to the JMH jar location
            for (Path p : allPaths) {
                Path candidate = p.getParent().getParent().resolve("recipeModuleClasspath.txt");
                if (Files.exists(candidate)) {
                    classpathFile = candidate;
                    break;
                }
            }
        }
        if (Files.exists(classpathFile)) {
            List<Path> rewriteSpringPaths = Files.readAllLines(classpathFile).stream()
                    .map(String::trim)
                    .filter(s -> !s.isEmpty())
                    .map(Paths::get)
                    .filter(Files::exists)
                    .collect(Collectors.toList());

            for (Path p : rewriteSpringPaths) {
                if (p.getFileName().toString().startsWith("rewrite-spring-")) {
                    rewriteSpringJar = p;
                    break;
                }
            }
            if (rewriteSpringJar != null) {
                rewriteSpringDependencyPaths = rewriteSpringPaths.stream()
                        .filter(p -> !p.equals(rewriteSpringJar))
                        .collect(Collectors.toList());
            }
        }
    }

    @Benchmark
    public void scanJar(Blackhole bh) {
        try (RecipeClassLoader classLoader = new RecipeClassLoader(recipeJar, dependencyPaths)) {
            Environment env = Environment.builder()
                    .scanJar(recipeJar, dependencyPaths, classLoader)
                    .build();
            Collection<RecipeDescriptor> descriptors = env.listRecipeDescriptors();
            bh.consume(descriptors.size());
        } catch (Exception e) {
            throw new RuntimeException(e);
        }
    }

    @Benchmark
    public void fullClassLoaderScan(Blackhole bh) {
        try (RecipeClassLoader classLoader = new RecipeClassLoader(recipeJar, dependencyPaths)) {
            Environment env = Environment.builder()
                    .load(new ClasspathScanningLoader(new Properties(), classLoader))
                    .build();
            Collection<RecipeDescriptor> descriptors = env.listRecipeDescriptors();
            bh.consume(descriptors.size());
        } catch (Exception e) {
            throw new RuntimeException(e);
        }
    }

    @Benchmark
    public void scanJarRewriteSpring(Blackhole bh) {
        if (rewriteSpringJar == null) {
            throw new IllegalStateException("rewrite-spring JAR not available. " +
                    "Run: ./gradlew :rewrite-benchmarks:writeRecipeModuleClasspath");
        }
        try (RecipeClassLoader classLoader = new RecipeClassLoader(rewriteSpringJar, rewriteSpringDependencyPaths)) {
            Environment env = Environment.builder()
                    .scanJar(rewriteSpringJar, rewriteSpringDependencyPaths, classLoader)
                    .build();
            Collection<RecipeDescriptor> descriptors = env.listRecipeDescriptors();
            bh.consume(descriptors.size());
        } catch (Exception e) {
            throw new RuntimeException(e);
        }
    }

    @Benchmark
    public void fullClassLoaderScanRewriteSpring(Blackhole bh) {
        if (rewriteSpringJar == null) {
            throw new IllegalStateException("rewrite-spring JAR not available. " +
                    "Run: ./gradlew :rewrite-benchmarks:writeRecipeModuleClasspath");
        }
        try (RecipeClassLoader classLoader = new RecipeClassLoader(rewriteSpringJar, rewriteSpringDependencyPaths)) {
            Environment env = Environment.builder()
                    .load(new ClasspathScanningLoader(new Properties(), classLoader))
                    .build();
            Collection<RecipeDescriptor> descriptors = env.listRecipeDescriptors();
            bh.consume(descriptors.size());
        } catch (Exception e) {
            throw new RuntimeException(e);
        }
    }

    public static void main(String[] args) throws RunnerException {
        Options opt = new OptionsBuilder()
                .include(RecipeLoadingBenchmark.class.getSimpleName())
                .addProfiler(GCProfiler.class)
                .build();
        new Runner(opt).run();
    }
}
